package bubblesort;

public class Schueler {
  String nachname;
  String vorname;

  public Schueler(String nachname, String vorname) {
    this.nachname = nachname;
    this.vorname = vorname;
  }

  public String getNachname() {
    return nachname;
  }

  public String getVorname() {
    return vorname;
  }
}
